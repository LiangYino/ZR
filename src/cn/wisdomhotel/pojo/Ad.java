package cn.wisdomhotel.pojo;

public class Ad {
	
	/**
	 * private attr
	 */
	
	private Integer id;
	private String name;
	private Integer inter;
	
	/**
	 * get and set method
	 */
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getInter() {
		return inter;
	}
	public void setInter(Integer inter) {
		this.inter = inter;
	}
}

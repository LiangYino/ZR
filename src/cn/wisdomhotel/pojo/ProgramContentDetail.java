package cn.wisdomhotel.pojo;

public class ProgramContentDetail {
	private Integer id;
	private Integer programContentId;
	private Integer materialId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getProgramContentId() {
		return programContentId;
	}
	public void setProgramContentId(Integer programContentId) {
		this.programContentId = programContentId;
	}
	public Integer getMaterialId() {
		return materialId;
	}
	public void setMaterialId(Integer materialId) {
		this.materialId = materialId;
	}
}

package cn.wisdomhotel.pojo;

public class ExamDetail {
	private Integer id;
	private Integer satisfyId;
	private String name;
	private Integer man;
	private Integer jiben;
	private Integer bumanyi;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getSatisfyId() {
		return satisfyId;
	}
	public void setSatisfyId(Integer satisfyId) {
		this.satisfyId = satisfyId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getMan() {
		return man;
	}
	public void setMan(Integer man) {
		this.man = man;
	}
	public Integer getJiben() {
		return jiben;
	}
	public void setJiben(Integer jiben) {
		this.jiben = jiben;
	}
	public Integer getBumanyi() {
		return bumanyi;
	}
	public void setBumanyi(Integer bumanyi) {
		this.bumanyi = bumanyi;
	}
}

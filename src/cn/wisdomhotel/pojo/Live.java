package cn.wisdomhotel.pojo;

public class Live {
	private Integer id;
	private String name;
	private String freq;
	private Integer position;
	private String zhishi;
	private String radiozhishi;
	private Integer channelcode;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFreq() {
		return freq;
	}
	public void setFreq(String freq) {
		this.freq = freq;
	}
	public Integer getPosition() {
		return position;
	}
	public void setPosition(Integer position) {
		this.position = position;
	}
	public String getZhishi() {
		return zhishi;
	}
	public void setZhishi(String zhishi) {
		this.zhishi = zhishi;
	}
	public String getRadiozhishi() {
		return radiozhishi;
	}
	public void setRadiozhishi(String radiozhishi) {
		this.radiozhishi = radiozhishi;
	}
	public Integer getChannelcode() {
		return channelcode;
	}
	public void setChannelcode(Integer channelcode) {
		this.channelcode = channelcode;
	}
}
